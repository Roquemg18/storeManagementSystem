package ar.unrn.supermarket;

/**
 * Interfaz que define las acciones que puede realizar un usuario en el supermercado.
 */
public interface AccionUsuario {
    /**
     * Método que ejecuta las acciones del usuario.
     * <p>
     * Precondición: Ninguna.
     * Postcondición: Se ejecutan las acciones correspondientes al usuario.
     */
    void ejecutar();
}
